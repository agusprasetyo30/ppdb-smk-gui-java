   private void btnLoginAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginAdminActionPerformed
      Admin admin = new Admin();
      String username = txtUsername.getText();
      String password = txtPasswordAdmin.getText();
      
      if (admin.loginAdmin(username, password)) {
         AdminForm adminForm = new AdminForm();
         Admin dataAdmin = admin.getByUserPass(username, password);

         adminForm.txtUsername.setText(dataAdmin.getUsername());
         adminForm.txtNama.setText(dataAdmin.getNama());
         adminForm.idFoto = txtUsername.getText();
         System.out.println(adminForm.idFoto);

         try {
            ImageIcon icon = new ImageIcon(new ImageIcon(getClass().getResource("/images/Admin/" + adminForm.idFoto + ".png"))
              .getImage().getScaledInstance(210, 270, Image.SCALE_SMOOTH));
            adminForm.lblFoto.setIcon(icon);
         } catch (Exception e) {
            System.out.println(e.getMessage());
         }
         adminForm.setVisible(true);
         this.setVisible(false);
         
      } else {
         JOptionPane.showMessageDialog(null, "Login gagal");
         txtPasswordAdmin.setText("");
         txtUsername.setText("");
      }

   }//GEN-LAST:event_btnLoginAdminActionPerformed

