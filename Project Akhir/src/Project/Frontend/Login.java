   private void btnLoginSiswaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginSiswaActionPerformed
      Siswa siswa = new Siswa();
      if (siswa.loginSiswa(txtIdSiswa.getText())) {
         inputNilaiSiswa input = new inputNilaiSiswa();
         Siswa data = siswa.getById(txtIdSiswa.getText());
         input.txtIdSiswa.setText(data.getId_siswa());
         input.txtNama.setText(data.getNama());
         input.txtAlamat.setText(data.getAlamat());
         input.txtAsalSekolah.setText(data.getAsal_sekolah());
         input.idFoto = txtIdSiswa.getText();
         System.out.println(input.idFoto);

         try {
            ImageIcon icon = new ImageIcon(new ImageIcon(getClass().getResource("/images/Siswa/" + input.idFoto.toUpperCase() + ".png"))
              .getImage().getScaledInstance(246, 270, Image.SCALE_SMOOTH));
            input.lblFoto.setIcon(icon);
         } catch (Exception e) {
            System.out.println(e.getMessage());
         }

         input.setVisible(true);
         this.setVisible(false);
      } else {
         JOptionPane.showMessageDialog(null, "Login gagal", "Peringatan", JOptionPane.ERROR_MESSAGE);
      }
   }//GEN-LAST:event_btnLoginSiswaActionPerformed

