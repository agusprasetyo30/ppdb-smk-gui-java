   private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
      Siswa siswa = new Siswa();
      getJurusan();

      if (lblJurusan.getText().equals("0") || txtRata.getText().equals("0")) {
         JOptionPane.showMessageDialog(null, "Data yang anda isi belum lengkap", "Peringatan", JOptionPane.ERROR_MESSAGE);
      } else {
         getJurusan();
         siswa.setNilai_bin(Double.valueOf(txtNilaiBin.getText()));
         siswa.setNilai_big(Double.valueOf(txtNilaiBig.getText()));
         siswa.setNilai_mtk(Double.valueOf(txtNilaiMtk.getText()));
         siswa.setNilai_ipa(Double.valueOf(txtNilaiIpa.getText()));
         siswa.setRata(Double.valueOf(txtRata.getText()));
         siswa.setId_siswa(txtIdSiswa.getText());
         siswa.getJurusan().setId_jurusan(getJurusan());
         siswa.saveNilaiSiswa();
         clearData();
         JOptionPane.showMessageDialog(null, "Pengisian data berhasil", "Informasi", JOptionPane.INFORMATION_MESSAGE);
         this.setVisible(false);
      }
   }//GEN-LAST:event_btnSimpanActionPerformed

   private void btnHitungrataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungrataActionPerformed
      double nilai_bin = Double.valueOf(txtNilaiBin.getText());
      double nilai_big = Double.valueOf(txtNilaiBig.getText());
      double nilai_mtk = Double.valueOf(txtNilaiMtk.getText());
      double nilai_ipa = Double.valueOf(txtNilaiIpa.getText());
      double hitung_rata;

      hitung_rata = (nilai_bin + nilai_big + nilai_mtk + nilai_ipa) / 4;
      txtRata.setText(String.valueOf(hitung_rata));
   }//GEN-LAST:event_btnHitungrataActionPerformed

