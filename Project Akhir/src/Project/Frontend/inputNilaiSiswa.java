   public int getJurusan() {
      int jurusan = 0;
      if (radioRpl.isSelected()) {
         jurusan = 1;
      } else if (radioMesin.isSelected()) {
         jurusan = 2;
      } else if (radioTkr.isSelected()) {
         jurusan = 3;
      } else if (radioTkj.isSelected()) {
         jurusan = 4;
      } else if (radioMM.isSelected()) {
         jurusan = 5;
      } else if (radioListrik.isSelected()) {
         jurusan = 6;
      }
      lblJurusan.setText(String.valueOf(jurusan));

      return jurusan;
   }

